// Auto-generate tip for unlisted kanji
function autoTip(k, data) {
  const kr = data.kr || '';
  const m = data.krMeaning || data.meanings || '';
  const on = (data.on || '').split(',')[0].trim();
  const kun = (data.kun || '').split(',')[0].trim().replace(/[.-]/g,'');
  const parts = [kr, m, on].filter(Boolean);
  return `'${m}'을(를) 뜻하는 한자. 음독 ${on}${kun ? ', 훈독 '+kun : ''}. "${m}" 연상으로 암기.`;
}

// Build full TIPS
const full = {};
for (const [k, data] of Object.entries(KM)) {
  if (MANUAL[k]) {
    full[k] = MANUAL[k];
  } else {
    full[k] = { tip: autoTip(k, data) };
  }
}

// Output as JS const
let out = 'const TIPS = {\n';
for (const [k, v] of Object.entries(full)) {
  const etym = v.e || v.etymology || '';
  const tipText = v.t || v.tip || '';
  let entry = `{tip:${JSON.stringify(tipText)}`;
  if (etym) entry += `,etymology:${JSON.stringify(etym)}`;
  entry += `}`;
  out += `  ${JSON.stringify(k)}:${entry},\n`;
}
out += '};\n';

fs.writeFileSync('tips_output.js', out);
